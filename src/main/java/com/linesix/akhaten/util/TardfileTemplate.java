package com.linesix.akhaten.util;

public class TardfileTemplate {

    private String user;
    private String uuid;
    private String tardis_id;
    private String x;
    private String y;
    private String z;

    public String getUser() {

        return user;

    }

    public void setUser(String user) {

        this.user = user;

    }

    public String getUuid() {

        return uuid;

    }

    public void setUuid(String uuid) {

        this.uuid = uuid;

    }

    public String getTardis_id() {

        return tardis_id;

    }

    public void setTardis_id(String tardis_id) {

        this.tardis_id = tardis_id;

    }

    public String getX() {

        return x;

    }

    public void setX(String x) {

        this.x = x;

    }

    public String getY() {

        return y;

    }

    public void setY(String y) {

        this.y = y;

    }

    public String getZ() {

        return z;

    }

    public void setZ(String z) {

        this.z = z;

    }

}
